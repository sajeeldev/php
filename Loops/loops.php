        <h1>PHP Loops</h1>
        <p>Loops : when you write a code you want a same block of code to run for a certain number of times. So, instead of adding several almost equal line of code in a script, we can use loops</p>

        <p>In PHP, we have the following loop types:</p>

        <p><span style = "color: #ff5555"> while </span> - Loops through a block of code as long as the condition is true</p>
        <p><span style = "color: #ff5555"> do-while </span> - Loops through a block of code once, then repeats the code as long as the condition is true</p>
        <p><span style = "color: #ff5555"> for </span> - Loops through a block of code a specified number of times</p>
        <p><span style = "color: #ff5555"> foreach </span> - Loops through a block of code for each element in an array</p>

